ALTER SESSION SET "_ORACLE_SCRIPT"=true;

-- USER SQL
CREATE USER "GEOSERVER" IDENTIFIED BY "GEOSERVER"  DEFAULT TABLESPACE "USERS" TEMPORARY TABLESPACE "TEMP";

-- QUOTAS
ALTER USER "GEOSERVER" QUOTA UNLIMITED ON "USERS";

-- ROLES
GRANT "CONNECT" TO "GEOSERVER" ;
GRANT "RESOURCE" TO "GEOSERVER" ;
ALTER USER "GEOSERVER" DEFAULT ROLE "CONNECT","RESOURCE";

GRANT CREATE VIEW TO "GEOSERVER" ;
GRANT CREATE SYNONYM TO "GEOSERVER" ;